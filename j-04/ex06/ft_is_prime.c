int	ft_is_prime(int nb)
{
	int	i;

	if (nb == 2)
		return (1);
	i = 3;
	while (i <= (nb / i))
	{
		if (!(nb & i))
			return (0);
		i += 2;
	}
	return (1 - (nb < 2 || !(nb % 2)));
}
